# Operadores #

## Operaciones básicas

print(f'1 + 1 = {1 + 1}')
print(type(f'1 + 1 = {1 + 1}'))
print(f'10 - 1 = {10 - 1}')
print(f'5 * 5 = {5 * 5}')
print(f'6 / 2 = {6 / 2}')
print(f'8 % 2 = {8 % 2}')

print('Hola ' * 5)

### Operaciones especiales
print(10 // 3) # ? Recuerda al metodo Math.floor() de JS
print(8 ** 2) # ? Exponente

## Operadores de comparación

print(10 == 10)
print(10 != 10)
print(10 < 9)
print(10 > 9)
print(10 >= 10)
print(10 <= 10)

## Operadores lógicos
print(10 == 10 and 9 != 9)
print(not(10 == 10 and 9 != 9))
print(10 == 10 or 9 != 9)

